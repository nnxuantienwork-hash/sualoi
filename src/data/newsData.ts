export const newsArticles = generateArticles();

export const getBreakingNews = () => {
  return newsArticles.filter(article => article.isBreaking);
};

export const getLatestByCategory = (slug: string, limit: number) => {
  return newsArticles
    .filter((a) => a.categorySlug === slug)
    // Dùng ?. để nếu không có createdAt thì trả về 0, không làm crash web
    .sort((a, b) => (b.createdAt ?? 0) - (a.createdAt ?? 0))
    .slice(0, limit);
};

export const getArticlesByCategory = (slug: string) => {
  return newsArticles
    .filter((a) => a.categorySlug === slug)
    .sort((a, b) => (b.createdAt ?? 0) - (a.createdAt ?? 0));
};

export const getMostRead = () => {
  return [...newsArticles].sort((a, b) => b.views - a.views).slice(0, 5);
};

export const formatViews = (views: number): string => {
  if (views >= 1000) {
    return `${(views / 1000).toFixed(1)}k`;
  }
  return views.toString();
};

export const getTimeAgo = (createdAt: number) => {
  const seconds = Math.floor((Date.now() - createdAt) / 1000);
  if (seconds < 60) return "Vừa xong";
  if (seconds < 3600) return Math.floor(seconds / 60) + " phút trước";
  if (seconds < 86400) return Math.floor(seconds / 3600) + " giờ trước";
  return Math.floor(seconds / 86400) + " ngày trước";
};
